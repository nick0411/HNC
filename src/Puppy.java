
public class Puppy {
	
	private String name;
	private int age;
	
	public Puppy(String Name,int Age) {
		name = Name;
		age = Age;
	}
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setName(String Name) {
		name = Name;
	}
	
	public void setAge(int Age) {
		age = Age;
	}

}
